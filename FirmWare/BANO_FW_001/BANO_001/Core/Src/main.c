void HEX_to_DEC(uint32_t Hex, char * DEC)
{
	char T = 48;
	DEC[3] = T;
	DEC[2] = T;
	DEC[1] = T;
	DEC[0] = T;
	while( Hex >= 1000 )
	{Hex = Hex - 1000; DEC[0] = DEC[0] + 1;}

	while( Hex >= 100 )
	{Hex = Hex - 100; DEC[1] = DEC[1] + 1;}

	while( Hex >= 10 )
	{Hex = Hex - 10; DEC[2] = DEC[2] + 1;}
	DEC[3] = DEC[3] + Hex;
}

